# Operações Aritiméticas - "+,-,/,*" são os simbolos para operações básicas, Soma, Subtração, Divisão
# e Multiplicação respectivamente.
print(1+1)
print(1-1)
print(1/1)
print(1*1)

# Exponenciação - Note que dependendo do Tipo de dado que ele carrega, trará um resultado diferente.
print(2 ** 3)
print(2 ** 3.)
print(2. ** 3)
print(2. ** 3.)

# Divisão Arredonda - Note que dependendo do Tipo de dado que ele carrega, trará um resultado diferente.
print(6 // 3)
print(6 // 3.)
print(6. // 3)
print(9.1 // 3.)

# Restante (módulo) - O resultado do operador é o restante após a divisão do número inteiro.
print(14 % 4) # 14 / 4 == 3 (inteiro menor) e sobra 2 ----- 14 % 4 == 2

# Potência - para notação científica, ultilzamos o e ou E.
print(3e8) #(é o mesmo que 3x10^8 ;3 vezes 10 á oitava)
print(3E8)

